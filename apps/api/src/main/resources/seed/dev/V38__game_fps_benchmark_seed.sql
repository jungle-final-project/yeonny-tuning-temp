-- Game-specific FPS evidence seed.
-- These rows are public reference points for recommendation/RAG evidence only.
-- They are not exact FPS guarantees for every map, driver, OS, patch, or board partner card.

CREATE TABLE IF NOT EXISTS game_fps_benchmarks (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  public_id UUID NOT NULL UNIQUE DEFAULT gen_random_uuid(),
  game_title VARCHAR(160) NOT NULL,
  game_key VARCHAR(120) NOT NULL,
  cpu_part_id BIGINT REFERENCES parts(id),
  gpu_part_id BIGINT REFERENCES parts(id),
  ram_gb INTEGER,
  resolution VARCHAR(30) NOT NULL,
  graphics_preset VARCHAR(80) NOT NULL,
  avg_fps NUMERIC(10, 2) NOT NULL,
  one_percent_low_fps NUMERIC(10, 2),
  source_name VARCHAR(120) NOT NULL,
  source_url TEXT NOT NULL,
  source_checked_at DATE NOT NULL,
  confidence VARCHAR(30) NOT NULL,
  metadata JSONB,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ,
  deleted_at TIMESTAMPTZ,
  CONSTRAINT chk_game_fps_ram_gb CHECK (ram_gb IS NULL OR ram_gb > 0),
  CONSTRAINT chk_game_fps_avg CHECK (avg_fps > 0),
  CONSTRAINT chk_game_fps_one_percent CHECK (one_percent_low_fps IS NULL OR one_percent_low_fps > 0),
  CONSTRAINT chk_game_fps_low_lte_avg CHECK (one_percent_low_fps IS NULL OR one_percent_low_fps <= avg_fps),
  CONSTRAINT chk_game_fps_confidence CHECK (confidence IN ('LOW', 'MEDIUM', 'HIGH'))
);

CREATE INDEX IF NOT EXISTS idx_game_fps_game_resolution
  ON game_fps_benchmarks(game_key, resolution)
  WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_game_fps_cpu_part
  ON game_fps_benchmarks(cpu_part_id)
  WHERE deleted_at IS NULL;

CREATE INDEX IF NOT EXISTS idx_game_fps_gpu_part
  ON game_fps_benchmarks(gpu_part_id)
  WHERE deleted_at IS NULL;

CREATE UNIQUE INDEX IF NOT EXISTS ux_game_fps_unique_seed
  ON game_fps_benchmarks(game_key, coalesce(cpu_part_id, 0), coalesce(gpu_part_id, 0), coalesce(ram_gb, 0), resolution, graphics_preset, source_name)
  WHERE deleted_at IS NULL;

